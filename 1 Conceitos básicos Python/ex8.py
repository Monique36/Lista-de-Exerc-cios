#Calculo do salário mensal


valor_por_hora = float(input("Quanto você ganha por hora? "))
horas_trabalhadas = float(input("Quantas horas você trabalhou no mês? "))

# Calcular o salário multiplicando o valor por hora pelas horas trabalhadas
salario = valor_por_hora * horas_trabalhadas

# Exibir o total do salário no mês
print("Seu salário no referido mês é:", salario)
